/** Find guides by category */
export function getGuidesByCategory(category: DIYGuide['category']): DIYGuide[] {
  return ALL_DIY_GUIDES.filter(g => g.category === category);
}

/** Find guide by ID */
export function getGuideById(id: string): DIYGuide | undefined {
  return ALL_DIY_GUIDES.find(g => g.id === id);
}

/** Search guides by keyword */
export function searchGuides(query: string): DIYGuide[] {
  const lower = query.toLowerCase();
  return ALL_DIY_GUIDES.filter(g =>
    g.title.toLowerCase().includes(lower) ||
    g.description.toLowerCase().includes(lower) ||
    g.id.toLowerCase().includes(lower) ||
    g.steps.some(s => s.toLowerCase().includes(lower))
  ).sort((a, b) => {
    // Exact title match first
    const aTitle = a.title.toLowerCase().includes(lower) ? 0 : 1;
    const bTitle = b.title.toLowerCase().includes(lower) ? 0 : 1;
    if (aTitle !== bTitle) return aTitle - bTitle;
    return a.difficulty - b.difficulty; // easier first
  });
}

/** Get a random guide for a category (for daily tips) */
export function getRandomGuide(category?: DIYGuide['category']): DIYGuide {
  const pool = category ? getGuidesByCategory(category) : ALL_DIY_GUIDES;
  return pool[Math.floor(Math.random() * pool.length)];
}

